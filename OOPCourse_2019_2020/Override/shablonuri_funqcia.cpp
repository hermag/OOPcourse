template <typename T>
T sum(T a, T b)
{
    return a+b;
}

int main()
{
    cout<<"მთელი ტიპის რიცხვები შეკრება - 5, 6 "<<sum(5,6)<<endl;
    cout<<"წილადი ტიპის რიცხვების შეკრება - 4.2, 3.4 "<<sum(4.2,3.4)<<endl;
    return 0;
}
